    private void txtCi_RucFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCi_RucFocusLost
        // VALIDACIÓN DE IDENTIFICACIONES (CÉDULA, RUC)
        String texto = txtCi_Ruc.getText();
        if (!ValidaCampoFormularios.validarIdentificacion(texto)) {
            JOptionPane.showMessageDialog(this, "Identificación no válida.  Debe tener 10 o 13 dígitos numéricos.", "Error", JOptionPane.ERROR_MESSAGE);
            txtCi_Ruc.requestFocusInWindow(); // Solicita que el campo de texto recupere el foco
        }      
    }//GEN-LAST:event_txtCi_RucFocusLost


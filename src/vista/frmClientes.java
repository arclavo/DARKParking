    private void txtEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEmailFocusLost
        // VALIDA QUE CORREO ELECTRÓNICO SE ESCRIBA CORRECTAMENTE
        String texto = txtEmail.getText();
        if (!ValidaCampoFormularios.validarCorreoElectronico(texto)) {
            JOptionPane.showMessageDialog(this, "Correo electrónico no válido.  Se acepta solo minúsculas, caracteres válidos (@,.,-,_)", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmail.requestFocusInWindow(); // Solicita que el campo de texto recupere el foco
        }       
    }//GEN-LAST:event_txtEmailFocusLost

    private void txtTelefonoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTelefonoFocusLost
        // VALIDA TELEFONO (9 Y 10 DIGITOS)
        String texto = txtTelefono.getText();
        if (!ValidaCampoFormularios.validarTelefono(texto)) {
            JOptionPane.showMessageDialog(this, "Valor no válido.  Solo se aceptan números. Longitud del campo 9 o 10 números. ", "Error", JOptionPane.ERROR_MESSAGE);
            txtTelefono.requestFocusInWindow(); // Solicita que el campo de texto recupere el foco
        }      
    }//GEN-LAST:event_txtTelefonoFocusLost

